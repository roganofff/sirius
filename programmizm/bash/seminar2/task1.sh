#!/bin/bash
if [[ ! -n $1 || ! -n $2 ]]
then
    echo "Недостаточно данных"
    exit 0
fi

if [[ $1 -le 0 || $2 -le 0 ]]
then
    echo "Один из углов меньше 0 градусов."
    exit 0
fi

if [[ $(($1+$2)) -ge 180 ]]
then
    echo "Сумма двух углов больше 180 градусов."
    exit 0
fi

echo "Третий угол = $((180-($1+$2)))."